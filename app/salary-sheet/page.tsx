  function validateSalaryRecord(
    data: SalaryRecord,
    currentId: string | null,
    sourceRecords: SalaryRecord[],
    options?: { ignoreExistingDuplicate?: boolean }
  ) {
    const nextErrors: Record<string, string> = {};

    requiredFields.forEach((field) => {
      if (!String(data[field] ?? "").trim()) {
        nextErrors[field] = `${field} is required`;
      }
    });

    if (toNumber(data["Basic Salary"]) <= 0) {
      nextErrors["Basic Salary"] = "Basic Salary must be greater than 0";
    }

    const year = toNumber(data["Payroll Year"]);

    if (year < 2000 || year > 2100) {
      nextErrors["Payroll Year"] = "Payroll Year must be between 2000 and 2100";
    }

    if (
      data["Payroll Month"] &&
      !payrollMonths.includes(String(data["Payroll Month"]))
    ) {
      nextErrors["Payroll Month"] = "Invalid Payroll Month";
    }

    if (
      data["Payroll Status"] &&
      !payrollStatuses.includes(String(data["Payroll Status"]))
    ) {
      nextErrors["Payroll Status"] = "Invalid Payroll Status";
    }

    const workingDays = toNumber(data["Working Days"]);
    const presentDays = toNumber(data["Present Days"]);
    const absentDays = toNumber(data["Absent Days"]);
    const leaveDays = toNumber(data["Leave Days"]);

    if (workingDays < 0 || presentDays < 0 || absentDays < 0 || leaveDays < 0) {
      nextErrors["Working Days"] = "Days cannot be negative";
    }

    if (
      workingDays > 0 &&
      presentDays + absentDays + leaveDays > workingDays
    ) {
      nextErrors["Present Days"] =
        "Present + Absent + Leave Days cannot exceed Working Days";
    }

    if (
      data["Payroll Status"] === "Paid" &&
      !String(data["Payment Method"] ?? "").trim()
    ) {
      nextErrors["Payment Method"] = "Payment Method is required for paid salary";
    }

    if (toNumber(data["Net Salary"]) < 0) {
      nextErrors["Net Salary"] = "Net Salary cannot be negative";
    }

    if (!options?.ignoreExistingDuplicate) {
      const duplicate = sourceRecords.some(
        (record) =>
          record.id !== currentId &&
          String(record["Employee ID"]).trim().toLowerCase() ===
            String(data["Employee ID"]).trim().toLowerCase() &&
          String(record["Payroll Month"]) === String(data["Payroll Month"]) &&
          Number(record["Payroll Year"]) === Number(data["Payroll Year"])
      );

      if (duplicate) {
        nextErrors["Employee ID"] =
          "This employee already has a salary sheet for this payroll month/year";
      }
    }

    return nextErrors;
  }

