function currency(value: unknown) {
  const amount = Number(value || 0);
  return new Intl.NumberFormat("en-BD", {
    style: "currency",
    currency: "BDT",
    maximumFractionDigits: 0,
  }).format(amount);
}

function toNumber(value: unknown) {
  if (typeof value === "number") return Number.isFinite(value) ? value : 0;

  const raw = String(value ?? "").trim();
  if (!raw) return 0;

  const negativeByParentheses = /^\(.*\)$/.test(raw);
  const cleaned = raw
    .replace(/[৳$,£€₹BDTbdt\s]/g, "")
    .replace(/[^\d.-]/g, "");

  const number = Number(cleaned);
  if (!Number.isFinite(number)) return 0;

  return negativeByParentheses ? -Math.abs(number) : number;
}

function round2(value: number) {
  return Math.round(value * 100) / 100;
}

function calculateIncomeTax(basicSalary: number) {
  if (basicSalary <= 30000) return 0;
  if (basicSalary <= 100000) return basicSalary * 0.05;
  return basicSalary * 0.1;
}

function normalizeKey(value: string) {
  return value.toLowerCase().replace(/[^a-z0-9]/g, "");
}

function escapeHtml(value: unknown) {
  return String(value ?? "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

