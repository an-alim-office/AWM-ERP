function applySalaryAutomation(input: SalaryRecord): SalaryRecord {
  const next = { ...input };

  const basicSalary = toNumber(next["Basic Salary"]);
  const workingDays = toNumber(next["Working Days"]);
  const presentDays = toNumber(next["Present Days"]);
  const absentDays = toNumber(next["Absent Days"]);
  const leaveDays = toNumber(next["Leave Days"]);
  const overtimeHours = toNumber(next["Overtime Hours"]);

  const dailyRate = workingDays > 0 ? basicSalary / workingDays : 0;
  const hourlyRate = dailyRate / 8;

  next["House Rent"] = round2(basicSalary * 0.4);
  next["Medical Allowance"] = round2(basicSalary * 0.1);
  next["Conveyance Allowance"] = round2(basicSalary * 0.05);
  next["Provident Fund (PF)"] = round2(basicSalary * 0.05);
  next["Income Tax"] = round2(calculateIncomeTax(basicSalary));
  next["Taxable Income"] = round2(basicSalary);
  next["Overtime Amount"] = round2(overtimeHours * hourlyRate * 1.5);
  next["Absent Deduction"] = round2(absentDays * dailyRate);

  const grossSalary =
    toNumber(next["Basic Salary"]) +
    toNumber(next["House Rent"]) +
    toNumber(next["Medical Allowance"]) +
    toNumber(next["Conveyance Allowance"]);

  const totalEarnings =
    grossSalary +
    toNumber(next["Tiffin Allowance"]) +
    toNumber(next["Mobile Bill Allowance"]) +
    toNumber(next["Project Bonus"]) +
    toNumber(next["Festival Bonus"]) +
    toNumber(next["Performance Bonus"]) +
    toNumber(next["Attendance Bonus"]) +
    toNumber(next["Incentive"]) +
    toNumber(next["Other Allowances"]) +
    toNumber(next["Overtime Amount"]) +
    toNumber(next["Non-Taxable Income"]) +
    toNumber(next["Arrear Salary"]) +
    toNumber(next["Salary Adjustment"]) +
    toNumber(next["Bonus Adjustment"]);

  const totalDeductions =
    toNumber(next["Provident Fund (PF)"]) +
    toNumber(next["Income Tax"]) +
    toNumber(next["Advance Salary Deduction"]) +
    toNumber(next["Loan Deduction"]) +
    toNumber(next["Absent Deduction"]) +
    toNumber(next["Late Attendance Deduction"]) +
    toNumber(next["Other Deductions"]) +
    toNumber(next["Deduction Adjustment"]);

  next["Gross Salary"] = round2(grossSalary);
  next["Total Earnings"] = round2(totalEarnings);
  next["Total Deductions"] = round2(totalDeductions);
  next["Net Salary"] = round2(totalEarnings - totalDeductions);

  const month = String(next["Payroll Month"] || getCurrentMonthName());
  const year = String(next["Payroll Year"] || new Date().getFullYear());
  const monthIndex = Math.max(1, payrollMonths.indexOf(month) + 1);
  next["Payroll Batch ID"] = `PAY-${year}-${String(monthIndex).padStart(
    2,
    "0"
  )}`;

  let riskScore = 0;
  if (toNumber(next["Net Salary"]) < 0) riskScore += 50;
  if (workingDays > 0 && presentDays + absentDays + leaveDays > workingDays)
    riskScore += 40;
  if (basicSalary > 0 && toNumber(next["Total Deductions"]) > basicSalary * 0.5)
    riskScore += 25;
  if (overtimeHours > 80) riskScore += 20;
  if (absentDays > 10) riskScore += 15;
  if (String(next["Payment Method"] || "") === "Cash") riskScore += 5;

  next["Risk Score"] = Math.min(100, riskScore);
  next["AI Anomaly Flag"] =
    riskScore >= 50
      ? "High Risk"
      : riskScore >= 25
      ? "Review Needed"
      : "No";

  next["Compliance Status"] =
    riskScore >= 50
      ? "Blocked for Review"
      : riskScore >= 25
      ? "Needs Review"
      : "Compliant";

  if (!next.Currency) next.Currency = "BDT";
  if (!next["Exchange Rate"]) next["Exchange Rate"] = 1;
  if (!next["Data Source"]) next["Data Source"] = "Manual";
  if (!next["Approval Level"]) next["Approval Level"] = "Level 1";
  if (!next["E-Signature Status"]) next["E-Signature Status"] = "Not Signed";

  return next;
}

function downloadCSV(records: SalaryRecord[], columns: string[]) {
  const header = columns.map((column) => `"${column}"`).join(",");

  const rows = records.map((record) =>
    columns
      .map((field) => {
        const value = String(record[field] ?? "").replaceAll('"', '""');
        return `"${value}"`;
      })
      .join(",")
  );

  const csv = [header, ...rows].join("\n");
  const blob = new Blob([csv], { type: "text/csv;charset=utf-8;" });
  const url = URL.createObjectURL(blob);

  const link = document.createElement("a");
  link.href = url;
  link.download = `salary-sheet-${Date.now()}.csv`;
  link.click();

  URL.revokeObjectURL(url);
}

function parseDelimitedLine(line: string, delimiter: string) {
  const cells: string[] = [];
  let current = "";
  let insideQuotes = false;

  for (let index = 0; index < line.length; index += 1) {
    const char = line[index];
    const next = line[index + 1];

    if (char === '"' && insideQuotes && next === '"') {
      current += '"';
      index += 1;
      continue;
    }

    if (char === '"') {
      insideQuotes = !insideQuotes;
      continue;
    }

    if (char === delimiter && !insideQuotes) {
      cells.push(current.trim());
      current = "";
      continue;
    }

    current += char;
  }

  cells.push(current.trim());
  return cells;
}

function detectDelimiter(line: string) {
  const candidates = ["\t", ",", ";", "|"];
  const counts = candidates.map((delimiter) => ({
    delimiter,
    count: parseDelimitedLine(line, delimiter).length,
  }));

  const best = counts.sort((a, b) => b.count - a.count)[0];
  return best.count > 1 ? best.delimiter : "MULTI_SPACE";
}

function parseTableText(text: string) {
  const lines = text
    .replace(/\r\n/g, "\n")
    .replace(/\r/g, "\n")
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean);

  if (!lines.length) return { rows: [] as string[][], delimiter: "\t" };

  const delimiter = detectDelimiter(lines[0]);

  const rows = lines.map((line) => {
    if (delimiter === "MULTI_SPACE") {
      return line.split(/\s{2,}/).map((cell) => cell.trim());
    }

    return parseDelimitedLine(line, delimiter);
  });

  return { rows, delimiter };
}

function normalizeDateValue(value: string) {
  const raw = value.trim();
  if (!raw) return "";

  if (/^\d{4}-\d{2}-\d{2}$/.test(raw)) return raw;

  const match = raw.match(/^(\d{1,2})[./-](\d{1,2})[./-](\d{2,4})$/);
  if (match) {
    const day = match[1].padStart(2, "0");
    const month = match[2].padStart(2, "0");
    const year = match[3].length === 2 ? `20${match[3]}` : match[3];
    return `${year}-${month}-${day}`;
  }

  const parsed = new Date(raw);
  if (!Number.isNaN(parsed.getTime())) {
    return parsed.toISOString().slice(0, 10);
  }

  return raw;
}

function normalizeImportValue(field: string, value: string) {
  const trimmed = String(value ?? "").trim();

  if (numberFields.includes(field)) return toNumber(trimmed);

  if (dateFields.includes(field)) return normalizeDateValue(trimmed);

  if (field === "Payroll Month") {
    const monthByNumber = Number(trimmed);
    if (Number.isInteger(monthByNumber) && monthByNumber >= 1 && monthByNumber <= 12) {
      return payrollMonths[monthByNumber - 1];
    }

    const found = payrollMonths.find(
      (month) => month.toLowerCase() === trimmed.toLowerCase()
    );
    return found || trimmed;
  }

  if (field === "Payroll Status") {
    const found = payrollStatuses.find(
      (status) => status.toLowerCase() === trimmed.toLowerCase()
    );
    return found || trimmed;
  }

  if (field === "Payment Method") {
    const found = paymentMethods.find(
      (method) => method.toLowerCase() === trimmed.toLowerCase()
    );
    return found || trimmed;
  }

  return trimmed;
}

