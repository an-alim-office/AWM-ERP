  async function saveOneImportedRecord(record: SalaryRecord) {
    const existing = records.find(
      (item) => getPayrollKey(item) === getPayrollKey(record)
    );

    const isUpdate = importMode === "upsert" && existing?.id;
    const url = isUpdate
      ? `${API_URL}?id=${encodeURIComponent(existing!.id)}`
      : API_URL;

    const response = await fetch(url, {
      method: isUpdate ? "PUT" : "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify(record),
    });

    const data = await response.json();

    if (!response.ok) {
      throw new Error(data.message || "Import save failed");
    }

    return data.record as SalaryRecord;
  }

  async function handleSaveBulkImport() {
    const validRows = bulkPreview.rows.filter(
      (row) => !Object.keys(row.errors).length
    );

    if (!validRows.length) {
      showToast("error", "No valid row found to import.");
      return;
    }

    if (bulkPreview.errorRows > 0) {
      const confirmed = window.confirm(
        `${bulkPreview.errorRows} invalid row(s) will be skipped. Continue importing ${validRows.length} valid row(s)?`
      );

      if (!confirmed) return;
    }

    setBulkSaving(true);
    setImportProgress({ total: validRows.length, done: 0, failed: 0 });

    const savedRecords: SalaryRecord[] = [];
    let failed = 0;

    try {
      for (let index = 0; index < validRows.length; index += BULK_IMPORT_CHUNK_SIZE) {
        const chunk = validRows.slice(index, index + BULK_IMPORT_CHUNK_SIZE);

        const results = await Promise.allSettled(
          chunk.map((row) => saveOneImportedRecord(applySalaryAutomation(row.data)))
        );

        results.forEach((result) => {
          if (result.status === "fulfilled") {
            savedRecords.push(result.value);
          } else {
            failed += 1;
            console.error(result.reason);
          }
        });

        setImportProgress((previous) => ({
          total: validRows.length,
          done: Math.min(validRows.length, previous.done + chunk.length),
          failed,
        }));
      }

      if (savedRecords.length) {
        setRecords((previous) => {
          const next = [...previous];

          savedRecords.forEach((saved) => {
            const index = next.findIndex((item) => item.id === saved.id);
            if (index >= 0) {
              next[index] = saved;
            } else {
              const duplicateIndex = next.findIndex(
                (item) => getPayrollKey(item) === getPayrollKey(saved)
              );

              if (duplicateIndex >= 0) next[duplicateIndex] = saved;
              else next.unshift(saved);
            }
          });

          return next;
        });
      }

      if (failed) {
        showToast(
          "warning",
          `${savedRecords.length} row imported, ${failed} row failed. Please review API/server logs.`
        );
      } else {
        showToast("success", `${savedRecords.length} salary rows imported successfully.`);
        setBulkText("");
        setIsBulkOpen(false);
      }

      loadRecords();
    } catch (error: any) {
      console.error(error);
      showToast("error", error.message || "Bulk import failed.");
    } finally {
      setBulkSaving(false);
    }
  }

